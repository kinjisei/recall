// Утилиты мини-игр раздела «Слова»: перемешивание, выборка, сравнение ответов
// и связь с FSRS.
//
// Правило: в играх штрафуем, но не поощряем — верный ответ расписание НЕ
// трогает, чтобы лёгкая игра случайно не «засчитала» слово выученным.
//
// ⚠️ 2026-07-24, важная правка. Раньше промах ставил карточке самую жёсткую
// оценку «again». Замер (scripts/test-fsrs-graduation.mjs) показал, во что это
// обходилось: зрелое слово с интервалом 57 дней после ОДНОГО промаха в игре
// откатывалось в «переучивание» с интервалом 0. А так как игры берут в первую
// очередь слова из колоды, зрелые слова попадали под удар постоянно: при одной
// ошибке в неделю слово не доходило до статуса «изучено» ВООБЩЕ (за 60 дней
// интервал так и стоял на трёх днях) — то есть повторяешь шестой день подряд
// одно и то же. Промах в игре на скорость с вариантами ответа означает
// «замешкался», а не «забыл»; цена у него теперь соответствующая.
// ============================================================================
import { reviewCard } from '../../lib/fsrs'
import { addCardsBulk, getDefaultDeck } from '../../lib/cards'
import { recordMiss } from '../../lib/gameMisses'
import type { PoolItem } from '../../lib/wordPool'
import type { AppLang } from '../../types'

// shuffle/sample/pickWords живут рядом с пулом (lib), normalize — в lib/text;
// здесь только реэкспорт, чтобы игры брали всё нужное из одного места.
export { shuffle, sample, pickWords } from '../../lib/wordPool'
export { normalizeAnswer as normalize } from '../../lib/text'

/**
 * Ошибка в игре — «ничего не терять»:
 *  • слово из колоды → приблизить повтор, но НЕ обнулять прогресс: обычный
 *    промах = «сложно» (интервал сокращается, слово остаётся зрелым), и лишь
 *    если по нему промахнулись 3 раза за неделю — «не знаю» (на переучивание);
 *  • слово из пака (карточки ещё нет) → добавить в колоду, чтобы ошибка не
 *    пропала — дальше оно пойдёт по расписанию в «Повторение». addCardsBulk
 *    сам пропускает дубли, так что повторная ошибка не плодит карточки.
 * Тихо игнорирует сбои: игра не должна падать из-за сети.
 */
export function markWrong(item: PoolItem, lang: AppLang): void {
  if (item.card) {
    const serious = recordMiss(item.card.id)
    void reviewCard(item.card, item.state ?? null, serious ? 'again' : 'hard').catch(() => {})
    return
  }
  void (async () => {
    try {
      const deck = await getDefaultDeck(lang)
      await addCardsBulk(deck.id, [
        { front: item.term, back: item.translation, example: item.example },
      ])
    } catch {
      /* сеть/лимит — не критично */
    }
  })()
}
