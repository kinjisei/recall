// ============================================================================
// Скролл к началу страницы при смене ВНУТРЕННЕГО экрана (view/mode/selected).
// Роуты покрывает глобальный <ScrollToTop/>, но многие экраны переключают
// содержимое стейтом — без сброса новый экран открывался на прежней прокрутке
// («я не вижу темы, где я и что мне делать»). Дёргается при каждом изменении dep.
// ============================================================================
import { useEffect } from 'react'

export function useScrollTop(dep: unknown): void {
  useEffect(() => {
    window.scrollTo(0, 0)
  }, [dep])
}
